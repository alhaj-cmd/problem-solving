// switch-case grading system

const grade = "A+"


switch (grade){
    case 'A+':
    console.log('It is a very good studenst')
    break;
    case 'A-':
    console.log('It is a good student')
    break;
    case 'B+':
    console.log('It is a normal student')
    break;
    case 'B':
    console.log('It is a  student')
    break;
    case 'F':
    console.log('This subject is fail')
    break;
    default:
        console.log('grade');
    
}